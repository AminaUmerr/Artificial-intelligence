# 1. Printing a basic greeting  
print("Hello, World!")  

# 2. Displaying numeric values  
print(2024)  
print(2.718)  

# 3. Printing multiple values with commas  
print("Name:", "Amna", "| Age:", 20)  

# 4. Concatenating strings and displaying output  
print("Learning " + "Python " + "is fun!")  

# 5. Using f-strings for formatted output  
user_name = "Amna"  
user_age = 20  
print(f"Hello! My name is {user_name}, and I am {user_age} years old.")  

# 6. Printing a multi-line string  
print("""Python allows  
multi-line strings  
using triple quotes.""")  

# 7. Demonstrating escape characters  
print("She said, \"I love coding!\"\nLet's get started.")  

# 8. Performing calculations and displaying results  
print("10 - 4 =", 10 - 4)  

# 9. Displaying a list of items  
colors = ["Red", "Green", "Blue"]  
print("Available colors:", colors)  

# 10. Using `sep` and `end` in print statements  
print("Welcome", "to", "Python", sep=" * ", end="!!!\n")  
print("Happy Coding!")

# 11. Printing a boolean value  
is_python_fun = True  
print("Is Python fun?", is_python_fun)  

# 10. Printing a tuple  
coordinates = (10.5, 20.3)  
